#!/bin/bash
#
# manage_cpusets.sh — cgroup v2 cpuset partition manager.
#
# Creates isolated cpuset partitions for real-time workloads, with support
# for multiple concurrent partitions, batch config files, runtime task
# placement, boot persistence, and optional integration with the EtherCAT
# NIC/IRQ setup provided by setup_ethercat_interface.sh.
#
# Usage:
#   manage_cpusets.sh create <name> <cpus> [--with-ethercat-rt] [--nic <iface>]
#   manage_cpusets.sh remove <name>
#   manage_cpusets.sh list
#   manage_cpusets.sh status
#   manage_cpusets.sh run <name> -- <cmd...>
#   manage_cpusets.sh apply <config-file> [--yes]
#   manage_cpusets.sh verify [<name>]
#   manage_cpusets.sh ethercat-rt <name> [--nic <iface>] [--rt-core <N>]
#   manage_cpusets.sh install-service [<config-file>]
#   manage_cpusets.sh uninstall-service
#   manage_cpusets.sh install-affinity-defaults
#   manage_cpusets.sh uninstall-affinity-defaults
#   manage_cpusets.sh migrate-cmdline [--add-rt-flags]
#   manage_cpusets.sh help
#
# Typical boot ordering: the cpusets.service installed by `install-service`
# runs before docker.service, user@.service, and systemd-logind.service so
# no cgroup has claimed the isolated CPUs before the partition is set up.

set -u  # unbound-var errors; we deliberately do NOT use -e here because
        # subcommands do their own error handling with explicit die().

# TUI bridge — op_confirm / op_ask helpers. Sourced when running from
# this checkout; gracefully no-op'd when this script is symlinked
# into /usr/lib/dma-ethercat/ for the .deb install where the bridge
# helper isn't shipped. The bridge itself is also a no-op when fd 3
# is closed (plain shell), so behavior outside the TUI is unchanged.
_OPS_PROMPT="${BASH_SOURCE[0]%/*}/../lib/ops-prompt.sh"
if [ -r "$_OPS_PROMPT" ]; then
  # shellcheck disable=SC1090
  . "$_OPS_PROMPT"
else
  op_confirm() {
    local prompt="$1" default="${2:-false}" hint reply
    [ "$default" = true ] && hint="[Y/n]" || hint="[y/N]"
    read -r -p "$prompt $hint: " reply || return 1
    if [ -z "$reply" ]; then
      [ "$default" = true ] && return 0 || return 1
    fi
    case "$reply" in y|Y|yes|true) return 0 ;; *) return 1 ;; esac
  }
fi
unset _OPS_PROMPT

# ---------- Script locations -----------------------------------------------
# Resolve symlinks so an installed entrypoint like /usr/sbin/dma-ethercat-cpusets
# (symlinked into /usr/lib/dma-ethercat/cpusets/) still finds lib/ adjacent.
_REAL_SOURCE="$(readlink -f "${BASH_SOURCE[0]}" 2>/dev/null || echo "${BASH_SOURCE[0]}")"
SCRIPT_DIR="$(cd "$(dirname "$_REAL_SOURCE")" && pwd)"
LIB_DIR="$SCRIPT_DIR/lib"

# ---------- Colors ---------------------------------------------------------
RED=$'\033[0;31m'
GREEN=$'\033[0;32m'
YELLOW=$'\033[1;33m'
BLUE=$'\033[0;34m'
NC=$'\033[0m'

# ---------- Paths ----------------------------------------------------------
CGROUP_ROOT="/sys/fs/cgroup"
STATE_DIR="/var/lib/manage_cpusets"
STATE_FILE="$STATE_DIR/state"
AFFINITY_DROPIN="/etc/systemd/system.conf.d/cpuaffinity.conf"
CPUSETS_CONF="/etc/cpusets.conf"
CPUSETS_SERVICE="/etc/systemd/system/cpusets.service"
INSTALL_DIR="/usr/local/lib/manage_cpusets"
APPLY_WRAPPER="/usr/local/sbin/apply-cpusets"

# Slices whose cpuset.cpus must be shrunk before an isolated partition can
# activate. Any slice not in this list that still claims one of the target
# CPUs will cause the partition to go "isolated invalid".
#
# Phantom-OS local addition: `kubepods` and `kubepods.slice` cover the
# kubelet root cgroup. k0s/kubelet creates one of these (path varies by
# cgroup driver — `kubepods` for cgroupfs, `kubepods.slice` for
# systemd) at startup covering all CPUs, blocking partition activation.
# We list both so either layout is shrunk safely; the directory check
# in shrink_sibling_slices() makes a missing entry a no-op.
MANAGED_SLICES=(system.slice user.slice init.scope docker.slice kubepods kubepods.slice)

# Minimum number of CPUs to keep un-isolated (safety floor).
HOUSEKEEPING_FLOOR=2

# ---------- Default partition layout ---------------------------------------
# Used when 'install-service' or 'apply' is invoked without a config-file
# argument. Override by passing an explicit INI file.
DEFAULT_PARTITION_NAME="rt"
DEFAULT_PARTITION_CPUS="11-13"

emit_default_config() {
    cat <<EOF
# Default cpuset partition layout for DMA.ethercat.
# Generated by manage_cpusets.sh — edit /etc/cpusets.conf to customize.
#
# Format (INI):
#   [name]
#   cpus = <range>          # e.g. 11-13 or 10,12,14
#   description = <text>    # informational

[${DEFAULT_PARTITION_NAME}]
cpus = ${DEFAULT_PARTITION_CPUS}
description = EtherCAT RT partition (default)
EOF
}

