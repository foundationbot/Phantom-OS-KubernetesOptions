# ---------- Subcommand: run -----------------------------------------------
cmd_run() {
    local name=""
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --) shift; break ;;
            -*) die "Unknown flag: $1" ;;
            *)
                if [[ -z "$name" ]]; then name="$1"; shift
                else break
                fi ;;
        esac
    done

    [[ -z "$name" ]] && die "Usage: run <name> -- <cmd...>"
    [[ $# -eq 0 ]] && die "No command after '--'"

    require_root

    # Prefer the slice cgroup (Option C), fall back to legacy standalone
    # cgroup for pre-migration robots.
    local path
    path=$(partition_cgroup_path "$name")
    if [[ ! -d "$path" ]]; then
        if [[ -d "$CGROUP_ROOT/$name" ]]; then
            warn "Using legacy pre-slice cgroup $CGROUP_ROOT/$name (run 'apply' to migrate)"
            path="$CGROUP_ROOT/$name"
        else
            die "Partition '$name' does not exist at $path or $CGROUP_ROOT/$name"
        fi
    fi

    # Exec into the partition: write our shell pid to cgroup.procs, then exec.
    # Using $SUDO bash -c lets us satisfy the write even when run as non-root.
    info "Executing '$*' inside cpuset '$name' ($path)..."
    $SUDO bash -c "echo \$\$ > '$path/cgroup.procs'; exec \"\$@\"" -- "$@"
}

# ---------- Subcommand: apply (INI config) --------------------------------
# Config format:
#   [name]
#   cpus = 10-11
#   description = EtherCAT master
cmd_apply() {
    local config=""
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --yes) SKIP_PROMPTS=1; shift ;;
            -*) die "Unknown flag: $1" ;;
            *)
                [[ -z "$config" ]] && { config="$1"; shift; continue; }
                die "Unexpected argument: $1"
                ;;
        esac
    done

    [[ -z "$config" ]] && die "Usage: apply <config-file> [--yes]"
    [[ -f "$config" ]] || die "Config file not found: $config"

    require_root
    preflight_cgroup_v2

    # Two-pass: parse + validate all sections, then create.
    declare -a names
    declare -A cpus_by_name
    local section="" key="" val="" line
    while IFS= read -r line || [[ -n "$line" ]]; do
        # Strip comments, trim.
        line="${line%%#*}"
        line="$(echo "$line" | xargs)"
        [[ -z "$line" ]] && continue

        if [[ "$line" =~ ^\[(.+)\]$ ]]; then
            section="${BASH_REMATCH[1]}"
            validate_partition_name "$section"
            names+=("$section")
            continue
        fi
        if [[ -z "$section" ]]; then
            die "Config line before any section: $line"
        fi
        if [[ "$line" =~ ^([a-zA-Z_]+)[[:space:]]*=[[:space:]]*(.*)$ ]]; then
            key="${BASH_REMATCH[1]}"
            val="${BASH_REMATCH[2]}"
            case "$key" in
                cpus)        cpus_by_name[$section]="$val" ;;
                description) : ;;  # currently informational
                *)           warn "Ignoring unknown config key '$key' in [$section]" ;;
            esac
        fi
    done < "$config"

    [[ ${#names[@]} -eq 0 ]] && die "No sections in $config"

    # Validation pass (no mutation).
    local n
    for n in "${names[@]}"; do
        local cpus="${cpus_by_name[$n]:-}"
        [[ -z "$cpus" ]] && die "[$n]: cpus = <range> is required"
    done

    # Create pass.
    local created=()
    trap '
        for c in "${created[@]}"; do
            warn "Rolling back partition $c"
            deactivate_partition "$c" || true
        done
    ' ERR

    for n in "${names[@]}"; do
        local cpus="${cpus_by_name[$n]}"
        # Idempotency check: same cpus + cgroup present + partition
        # state actually 'isolated' (not 'isolated invalid'). If a
        # sibling cgroup like kubepods (created post-boot by k0s)
        # has stolen one of the partition's CPUs, state will be
        # 'isolated invalid' and we MUST re-apply (re-shrink siblings
        # + re-activate) — silently skipping leaves the partition
        # broken. Reading cpuset.cpus.partition is cheap and
        # authoritative.
        local current part_state="" cg
        current=$(state_get_cpus "$n")
        cg=$(partition_cgroup_path "$n")
        if [[ -f "$cg/cpuset.cpus.partition" ]]; then
            part_state=$(cat "$cg/cpuset.cpus.partition" 2>/dev/null)
        fi
        if [[ "$current" == "$cpus" && -d "$cg" \
              && "$part_state" == "isolated" \
              && ! -d "$CGROUP_ROOT/$n" ]]; then
            info "[$n] already active on $cpus — skipping"
            continue
        fi
        if [[ "$part_state" == "isolated invalid" ]]; then
            warn "[$n] is 'isolated invalid' (sibling cgroup stole CPUs) — re-applying"
        fi
        if [[ -d "$CGROUP_ROOT/$n" ]]; then
            info "[$n] legacy pre-slice cgroup present — will migrate"
        fi

        validate_requested_cpus "$n" "$cpus"
        # Per-partition isolcpus check.
        check_isolcpus_cmdline "$cpus"

        if [[ -n "$current" ]]; then
            deactivate_partition "$n"
        fi
        activate_partition "$n" "$cpus"
        created+=("$n")
    done

    trap - ERR
    success "Applied $(echo "${names[@]}" | wc -w) partition(s)."
}

# ---------- Subcommand: ethercat-rt ---------------------------------------
cmd_ethercat_rt() {
    local name="" nic="" rt_core=""
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --nic) nic="$2"; shift 2 ;;
            --rt-core) rt_core="$2"; shift 2 ;;
            -*) die "Unknown flag: $1" ;;
            *)
                [[ -z "$name" ]] && { name="$1"; shift; continue; }
                die "Unexpected argument: $1"
                ;;
        esac
    done

    [[ -z "$name" ]] && \
        die "Usage: ethercat-rt <partition> [--nic <iface>] [--rt-core <N>]"

    require_root

    local cpus
    cpus=$(state_get_cpus "$name")
    [[ -z "$cpus" ]] && die "Partition '$name' not found"

    # Lazy-source nic_rt.sh (not needed for pure cpuset ops).
    # shellcheck source=./lib/nic_rt.sh
    source "$LIB_DIR/nic_rt.sh"

    # Offer the partition's cpus as the RT candidate set.
    export RT_CANDIDATE_CORES="$cpus"
    nic="${nic:-ecat0}"

    if ! ip link show "$nic" &>/dev/null; then
        die "Interface $nic not found. Run setup_ethercat_interface.sh to create it first."
    fi

    info "Running EtherCAT RT setup for partition '$name' on $nic..."
    if [[ -n "$rt_core" ]]; then
        # Non-interactive path used by Phantom-OS-KubernetesOptions
        # bootstrap (FIR-269). Validate the operator-supplied core lies
        # inside the partition before short-circuiting select_rt_core.
        # cpu_utils.sh already provides cpu_list_contains.
        if ! cpu_list_contains "$cpus" "$rt_core"; then
            die "--rt-core $rt_core is not in partition '$name' cpus ($cpus)"
        fi
        export RT_CORE="$rt_core"
        info "RT core selected non-interactively: $RT_CORE"
    else
        if ! select_rt_core; then
            die "Core selection aborted"
        fi
    fi

    local irqs
    irqs=$(find_nic_irqs "$nic")
    [[ -z "$irqs" ]] && die "No IRQs found for $nic"

    info "IRQs: $irqs"
    # shellcheck disable=SC2086
    pin_irqs_to_core "$RT_CORE" $irqs
    # shellcheck disable=SC2086
    configure_irqbalance $irqs
    write_ethercat_rt_service "$nic" "$RT_CORE"
    apply_nic_tuning "$nic"
    create_nic_tuning_dispatcher "$nic"
    lock_isolated_core_governors
    restrict_workqueue_mask
    check_kernel_params "$RT_CORE"

    success "ethercat-rt complete on partition '$name' core $RT_CORE"
}

# ---------- Subcommand: verify --------------------------------------------
cmd_verify() {
    local name="${1:-}"
    local fail=0

    echo "Verification"
    echo "------------"

    # Phase 0: preconditions
    echo "[Phase 0] Preconditions"
    if [[ -f "$CGROUP_ROOT/cgroup.controllers" ]]; then
        success "  cgroup v2 mounted"
    else
        error "  cgroup v2 NOT mounted"; fail=1
    fi
    if grep -qw cpuset "$CGROUP_ROOT/cgroup.controllers" 2>/dev/null; then
        success "  cpuset controller available"
    else
        error "  cpuset controller NOT available"; fail=1
    fi
    if grep -qw cpuset "$CGROUP_ROOT/cgroup.subtree_control" 2>/dev/null; then
        success "  cpuset in subtree_control"
    else
        warn "  cpuset not in subtree_control (will be enabled on create)"
    fi

    # Phase 1/2: partition lifecycle + integrity
    echo ""
    echo "[Phase 1/2] Partition state"
    local names
    if [[ -n "$name" ]]; then
        names="$name"
    else
        names=$(state_list_names)
    fi
    if [[ -z "$names" ]]; then
        info "  No managed partitions."
    else
        local n cpus state effective path
        for n in $names; do
            path="$CGROUP_ROOT/$n"
            cpus=$(state_get_cpus "$n")
            if [[ ! -d "$path" ]]; then
                error "  $n: cgroup $path missing"; fail=1; continue
            fi
            state=$(cat "$path/cpuset.cpus.partition" 2>/dev/null || echo ?)
            effective=$(cat "$path/cpuset.cpus.effective" 2>/dev/null || echo ?)
            if [[ "$state" != "isolated" ]]; then
                error "  $n: partition state '$state' (expected 'isolated')"; fail=1
            else
                success "  $n: partition isolated on $effective"
            fi
            if [[ "$effective" != "$cpus" ]]; then
                warn "  $n: effective cpus $effective differs from requested $cpus"
            fi
            # Verify sibling slices don't claim these CPUs.
            local slice
            for slice in "${MANAGED_SLICES[@]}"; do
                local slice_path="$CGROUP_ROOT/$slice"
                [[ ! -f "$slice_path/cpuset.cpus.effective" ]] && continue
                local sib_eff overlap
                sib_eff=$(cat "$slice_path/cpuset.cpus.effective" 2>/dev/null || echo "")
                overlap=$(cpu_list_intersect "$cpus" "$sib_eff")
                if [[ -n "$overlap" ]]; then
                    error "  $n: $slice still claims $overlap"; fail=1
                fi
            done
        done
    fi

    # Phase 3: task placement
    echo ""
    echo "[Phase 3] Task placement"
    local isolated
    isolated=$(detect_isolated_cores)
    if [[ -z "$isolated" ]]; then
        warn "  No cores isolated — cannot check placement"
    else
        local isolated_list
        isolated_list=$(expand_cpu_range "$isolated")
        # Sample: check for user-space procs running on isolated cores.
        local stray
        stray=$(ps -eo psr,pid,comm,cgroup 2>/dev/null \
                  | awk -v cores="$isolated_list" 'BEGIN{split(cores,a," "); for(i in a) ci[a[i]]=1} NR>1 && ($1 in ci) && $3 !~ /^(migration|ksoftirqd|cpuhp|idle_inject|rcu|kworker)/' \
                  | head -5)
        if [[ -n "$stray" ]]; then
            warn "  Tasks on isolated cores (may be legitimate RT workload):"
            echo "$stray" | sed 's/^/    /'
        else
            success "  No unexpected tasks on isolated cores"
        fi
    fi

    # Phase 3.5: affinity drop-in effect
    echo ""
    echo "[Phase 3.5] systemd default CPUAffinity"
    if [[ -f "$AFFINITY_DROPIN" ]]; then
        local installed_affinity
        installed_affinity=$(grep -E '^CPUAffinity=' "$AFFINITY_DROPIN" | cut -d= -f2)
        success "  $AFFINITY_DROPIN present: CPUAffinity=$installed_affinity"
        local expected
        expected=$(compute_housekeeping_list)
        if [[ "$installed_affinity" != "$expected" ]]; then
            warn "  Drop-in value differs from current housekeeping ($expected)"
        fi
    else
        warn "  $AFFINITY_DROPIN not installed (optional — install-affinity-defaults)"
    fi

    echo ""
    if [[ $fail -eq 0 ]]; then
        success "All checks passed."
    else
        error "Some checks failed."
        return 1
    fi
}

