if [[ $EUID -eq 0 ]]; then
    SUDO=""
else
    SUDO="sudo"
fi

# ---------- Source libraries ----------------------------------------------
if [[ ! -d "$LIB_DIR" ]]; then
    echo "ERROR: library directory not found: $LIB_DIR" >&2
    exit 1
fi
# shellcheck source=./lib/cpu_utils.sh
source "$LIB_DIR/cpu_utils.sh"
# shellcheck source=./lib/systemd_units.sh
source "$LIB_DIR/systemd_units.sh"
# nic_rt.sh is only sourced on demand (ethercat-rt subcommand) because it
# pulls in udev helpers that aren't needed for pure cpuset ops.

# ---------- Logging helpers -----------------------------------------------
info()    { echo -e "${BLUE}$*${NC}"; }
warn()    { echo -e "${YELLOW}$*${NC}" >&2; }
success() { echo -e "${GREEN}$*${NC}"; }
error()   { echo -e "${RED}$*${NC}" >&2; }
die()     { error "$*"; exit 1; }

# ---------- Preflights -----------------------------------------------------
require_root() {
    if [[ $EUID -ne 0 && -z "$(command -v sudo)" ]]; then
        die "This operation requires root and 'sudo' is not available."
    fi
}

preflight_cgroup_v2() {
    # Detect via the kernel's own marker file rather than parsing
    # `mount` output — the latter varies between busybox / util-linux /
    # Jetson and was producing false negatives on Phantom-OS hosts.
    if [[ ! -f "$CGROUP_ROOT/cgroup.controllers" ]]; then
        die "cgroup v2 is not mounted at $CGROUP_ROOT. This tool requires cgroup v2."
    fi

    local controllers
    if ! controllers=$(cat "$CGROUP_ROOT/cgroup.controllers" 2>/dev/null); then
        die "Cannot read $CGROUP_ROOT/cgroup.controllers — cgroup v2 not functional."
    fi

    if ! echo "$controllers" | grep -qw cpuset; then
        die "cpuset controller not available. Kernel lacks CONFIG_CPUSETS."
    fi

    # Enable the cpuset controller in subtree_control if it's not already.
    local subtree
    subtree=$(cat "$CGROUP_ROOT/cgroup.subtree_control" 2>/dev/null || echo "")
    if ! echo "$subtree" | grep -qw cpuset; then
        info "Enabling cpuset controller in root subtree_control..."
        echo "+cpuset" | $SUDO tee "$CGROUP_ROOT/cgroup.subtree_control" > /dev/null \
            || die "Failed to enable cpuset controller in subtree_control."
    fi
}

# Warn (interactively) when isolcpus= is present on the kernel cmdline and
# overlaps with the requested partition CPUs.
# Arguments: requested_cpu_list
# Respects --yes flag via global SKIP_PROMPTS=1 (set by `apply --yes` etc.).
check_isolcpus_cmdline() {
    local requested="$1"
    local cmdline_isolcpus overlap stray
    cmdline_isolcpus=$(parse_isolcpus_cmdline)

    [[ -z "$cmdline_isolcpus" ]] && return 0

    # Union of requested + all already-managed partitions.
    local all_managed
    all_managed=$(compute_all_managed_cpus)
    local combined
    combined=$(cpu_list_union "$requested" "$all_managed")

    overlap=$(cpu_list_intersect "$cmdline_isolcpus" "$combined")
    stray=$(cpu_list_diff "$cmdline_isolcpus" "$combined")

    echo ""
    warn "isolcpus= is active on the kernel cmdline: $cmdline_isolcpus"
    if [[ -n "$overlap" ]]; then
        warn "  overlap with managed partitions: $overlap"
        warn "  This is redundant — cpuset partitions cannot dynamically release these cores."
    fi
    if [[ -n "$stray" ]]; then
        warn "  isolated outside any managed partition: $stray"
    fi

    if [[ "${SKIP_PROMPTS:-0}" == "1" ]]; then
        warn "Proceeding (non-interactive mode)."
        return 0
    fi

    echo ""
    echo "Options:"
    echo "  [1] Continue anyway (cpuset partitions layer on top of isolcpus=)"
    echo "  [2] Abort — remove isolcpus= with 'manage_cpusets.sh migrate-cmdline'"
    echo "  [3] Abort — let me pick different CPUs for the partition"
    echo ""
    if type op_ask >/dev/null 2>&1; then
        choice="$(op_ask cpuset_isolcpus_choice "Choice [1/2/3]" "1")"
    else
        read -p "Choice [1/2/3]: " -n 1 -r choice
        echo
    fi
    case "$choice" in
        1) return 0 ;;
        2) info "Run: $0 migrate-cmdline"; exit 1 ;;
        3) exit 1 ;;
        *) die "Invalid choice." ;;
    esac
}

# ---------- State file -----------------------------------------------------
# Simple line-oriented format: <name>|<cpus>|<epoch_created>
state_init() {
    if [[ ! -d "$STATE_DIR" ]]; then
        $SUDO mkdir -p "$STATE_DIR" || die "Failed to create $STATE_DIR"
    fi
    if [[ ! -f "$STATE_FILE" ]]; then
        $SUDO touch "$STATE_FILE"
    fi
}

state_list_names() {
    state_init
    awk -F'|' 'NF>=2 && $1 != "" {print $1}' "$STATE_FILE"
}

state_get_cpus() {
    local name="$1"
    state_init
    awk -F'|' -v name="$name" 'NF>=2 && $1 == name {print $2; exit}' "$STATE_FILE"
}

state_add() {
    local name="$1" cpus="$2"
    state_init
    state_remove "$name"  # idempotent update
    echo "$name|$cpus|$(date +%s)" | $SUDO tee -a "$STATE_FILE" > /dev/null
}

state_remove() {
    local name="$1"
    state_init
    local tmp
    tmp=$(mktemp)
    awk -F'|' -v name="$name" '$1 != name' "$STATE_FILE" > "$tmp"
    $SUDO cp "$tmp" "$STATE_FILE"
    rm -f "$tmp"
}

# Union of CPUs across every managed partition (from state file).
compute_all_managed_cpus() {
    local combined="" name cpus
    while read -r name; do
        [[ -z "$name" ]] && continue
        cpus=$(state_get_cpus "$name")
        [[ -n "$cpus" ]] && combined=$(cpu_list_union "$combined" "$cpus")
    done < <(state_list_names)
    echo "$combined"
}

# ---------- Partition primitives -------------------------------------------
validate_partition_name() {
    local name="$1"
    [[ "$name" =~ ^[a-zA-Z][a-zA-Z0-9_-]*$ ]] \
        || die "Invalid partition name '$name' (must start with a letter, alphanumeric + _ -)"
    [[ "$name" == "system.slice" || "$name" == "user.slice" || "$name" == "init.scope" ]] \
        && die "Refusing to manage reserved cgroup '$name'"
}

# Validate a requested CPU list: all online, no overlap with other managed
# partitions, housekeeping floor respected.
validate_requested_cpus() {
    local name="$1" cpus="$2"
    local expanded c

    expanded=$(expand_cpu_range "$cpus") \
        || die "Malformed CPU range: $cpus"
    [[ -z "$expanded" ]] && die "Empty CPU range"

    for c in $expanded; do
        cpu_is_online "$c" || die "CPU $c is not online"
    done

    # Overlap with other managed partitions (excluding <name> in case we are
    # updating it).
    local other_name other_cpus overlap
    for other_name in $(state_list_names); do
        [[ "$other_name" == "$name" ]] && continue
        other_cpus=$(state_get_cpus "$other_name")
        overlap=$(cpu_list_intersect "$cpus" "$other_cpus")
        if [[ -n "$overlap" ]]; then
            die "Requested CPUs overlap partition '$other_name' ($other_cpus): $overlap"
        fi
    done

    # Housekeeping floor: count post-create housekeeping cores.
    local present_cpus total_isolated housekeeping_count
    present_cpus=$(get_present_cpus)
    total_isolated=$(cpu_list_union "$(compute_all_managed_cpus)" "$cpus")
    housekeeping_count=$(cpu_list_count "$(cpu_list_diff "$present_cpus" "$total_isolated")")

    if [[ "$housekeeping_count" -lt "$HOUSEKEEPING_FLOOR" ]]; then
        die "Would leave $housekeeping_count housekeeping cores (floor: $HOUSEKEEPING_FLOOR). Refusing."
    fi
}

# Shrink the MANAGED_SLICES to a given cpu range. Idempotent. Also
# clears any cpuset.cpus.exclusive claim on those siblings — otherwise
# an existing exclusive claim on a sibling (kubelet's kubepods is the
# common offender) blocks our partition's cpus.exclusive write with
# "Cpu list in cpuset.cpus not exclusive".
shrink_sibling_slices() {
    local housekeeping="$1"
    local slice slice_path cpus_file exc_file
    for slice in "${MANAGED_SLICES[@]}"; do
        slice_path="$CGROUP_ROOT/$slice"
        [[ ! -d "$slice_path" ]] && continue
        cpus_file="$slice_path/cpuset.cpus"
        exc_file="$slice_path/cpuset.cpus.exclusive"
        if [[ -f "$cpus_file" ]]; then
            local current
            current=$(cat "$cpus_file" 2>/dev/null || echo "")
            # Writing the same value again is cheap; always write to stay consistent.
            if ! echo "$housekeeping" | $SUDO tee "$cpus_file" > /dev/null 2>&1; then
                warn "  Could not shrink $slice/cpuset.cpus (was: '$current')"
            fi
        fi
        if [[ -f "$exc_file" ]]; then
            # Empty value clears any existing exclusive claim. Tolerated
            # by all kernels with the cpus.exclusive file.
            : | $SUDO tee "$exc_file" > /dev/null 2>&1 || true
        fi
    done
}

# When removing a partition, restore MANAGED_SLICES to cover housekeeping +
# the un-managed cores. Safest default: write the fresh housekeeping range.
restore_sibling_slices() {
    local new_housekeeping
    new_housekeeping=$(cpu_list_diff "$(get_present_cpus)" "$(compute_all_managed_cpus)")
    shrink_sibling_slices "$new_housekeeping"
}

# Identify which other cgroup is still claiming one of the requested CPUs.
# Called when partition activation fails with "isolated invalid".
explain_partition_failure() {
    local name="$1" cpus="$2"
    local partition_file="$CGROUP_ROOT/$name/cpuset.cpus.partition"
    local reason
    reason=$(cat "$partition_file" 2>/dev/null || echo "(unavailable)")
    error "Partition $name did not activate: $reason"

    echo ""
    echo "Searching for sibling cgroups that still claim these CPUs..."
    local dir other_cpus overlap_any=0
    for dir in "$CGROUP_ROOT"/*/; do
        [[ ! -f "$dir/cpuset.cpus" ]] && continue
        local sibling
        sibling=$(basename "$dir")
        [[ "$sibling" == "$name" ]] && continue
        other_cpus=$(cat "$dir/cpuset.cpus" 2>/dev/null || echo "")
        [[ -z "$other_cpus" ]] && continue
        local overlap
        overlap=$(cpu_list_intersect "$cpus" "$other_cpus")
        if [[ -n "$overlap" ]]; then
            warn "  $sibling claims $other_cpus (overlap: $overlap)"
            overlap_any=1
        fi
    done
    [[ $overlap_any -eq 0 ]] && info "  No overlap found. Common alternate causes: partition CPU is offline, or subtree_control missing cpuset."
}

# Cgroup-v2 partition path for a managed partition. The slice unit
# rendered by activate_partition lives at /etc/systemd/system/<name>.slice
# and its cgroup is /sys/fs/cgroup/<name>.slice. The slice IS the
# partition (Option C from the design discussion) — we no longer create
# a separate top-level cgroup at $CGROUP_ROOT/<name>.
partition_slice_unit_path() {
    echo "/etc/systemd/system/${1}.slice"
}
partition_cgroup_path() {
    echo "$CGROUP_ROOT/${1}.slice"
}

# Render the systemd slice unit for a partition. Idempotent — writes a
# fresh file each time so changes to <cpus> propagate.
render_partition_slice_unit() {
    local name="$1" cpus="$2"
    local dst
    dst=$(partition_slice_unit_path "$name")
    local tmp
    tmp=$($SUDO mktemp) || return 1
    $SUDO tee "$tmp" > /dev/null <<EOF
# Auto-generated by manage_cpusets.sh
# Mirrors the cpuset partition '$name' (cpus $cpus).
# Sets cpuset.cpus via AllowedCPUs=; manage_cpusets.sh then writes the
# kernel partition flag (cpuset.cpus.exclusive + cpuset.cpus.partition)
# directly to the slice's cgroup after the slice is up.
#
# Services that need to run on the isolated cores must declare
#   Slice=${name}.slice
# in their unit (typically via a drop-in at
# /etc/systemd/system/<unit>.d/10-slice.conf).
[Unit]
Description=cpuset partition '$name' (cpus $cpus)
DefaultDependencies=no
Before=basic.target shutdown.target
Conflicts=shutdown.target

[Slice]
AllowedCPUs=$cpus
EOF
    $SUDO mv "$tmp" "$dst" || { $SUDO rm -f "$tmp"; return 1; }
    $SUDO chmod 0644 "$dst"
    return 0
}

# Migration: if the legacy /sys/fs/cgroup/<name> standalone cgroup
# exists from a pre-Option-C install, tear it down before the slice
# claims the same cpus. Holds tasks-out + un-isolate + rmdir.
teardown_legacy_partition() {
    local name="$1"
    local legacy="$CGROUP_ROOT/$name"
    [[ ! -d "$legacy" ]] && return 0

    info "Migrating legacy partition cgroup $legacy -> slice"
    # Move tasks out
    if [[ -f "$legacy/cgroup.procs" ]]; then
        local procs
        procs=$(cat "$legacy/cgroup.procs" 2>/dev/null || true)
        if [[ -n "$procs" ]]; then
            while read -r pid; do
                [[ -z "$pid" ]] && continue
                echo "$pid" | $SUDO tee "$CGROUP_ROOT/cgroup.procs" > /dev/null 2>&1 || true
            done <<< "$procs"
        fi
    fi
    # Un-isolate so rmdir succeeds even if it was a partition root
    [[ -f "$legacy/cpuset.cpus.partition" ]] && \
        echo member | $SUDO tee "$legacy/cpuset.cpus.partition" > /dev/null 2>&1 || true
    $SUDO rmdir "$legacy" 2>/dev/null || \
        warn "  rmdir $legacy failed (busy?) — manual cleanup needed"
}

# Create a partition by rendering a slice unit, activating it, then
# writing the kernel partition flag onto the slice's cgroup. The slice
# IS the partition; this collapses systemd cgroup ownership and
# manage_cpusets partition state into one cgroup, eliminating the
# sibling-cgroup conflict where two cgroups both claimed the same cpus.
activate_partition() {
    local name="$1" cpus="$2"
    local cg
    cg=$(partition_cgroup_path "$name")

    # If a pre-Option-C standalone cgroup exists, tear it down first.
    teardown_legacy_partition "$name"

    # Shrink the MANAGED_SLICES (system.slice, user.slice, init.scope,
    # docker.slice, kubepods, kubepods.slice) off the isolated cpus
    # *before* the partition activates. Systemd's own slices do react
    # to the partition flag's exclusivity cascade once it's set, but
    # kubelet's kubepods is a cgroupfs cgroup (not a systemd slice)
    # that doesn't react — its cpuset.cpus=0-13 (and possibly a
    # cpuset.cpus.exclusive=0-13) blocks the partition's exclusivity
    # check with "Cpu list in cpuset.cpus not exclusive". Compute the
    # housekeeping set including this new partition and shrink.
    local new_total new_housekeeping
    new_total=$(cpu_list_union "$(compute_all_managed_cpus)" "$cpus")
    new_housekeeping=$(cpu_list_diff "$(get_present_cpus)" "$new_total")
    info "Shrinking sibling slices to $new_housekeeping..."
    shrink_sibling_slices "$new_housekeeping"

    info "Rendering slice unit $(partition_slice_unit_path "$name") (AllowedCPUs=$cpus)..."
    render_partition_slice_unit "$name" "$cpus" || die "failed to render slice unit"

    $SUDO systemctl daemon-reload || die "systemctl daemon-reload failed"

    info "Starting ${name}.slice..."
    $SUDO systemctl start "${name}.slice" || die "systemctl start ${name}.slice failed"

    # Wait briefly for the cgroup to materialize. Slice activation is
    # synchronous in practice but be defensive.
    local _i
    for _i in 1 2 3 4 5; do
        [[ -f "$cg/cpuset.cpus.exclusive" ]] && break
        sleep 0.2
    done
    [[ -f "$cg/cpuset.cpus.exclusive" ]] || die "$cg/cpuset.cpus.exclusive did not appear"

    info "Setting cpuset.cpus.exclusive=$cpus on ${name}.slice cgroup..."
    if ! echo "$cpus" | $SUDO tee "$cg/cpuset.cpus.exclusive" > /dev/null; then
        die "failed to write cpuset.cpus.exclusive"
    fi

    info "Activating isolated partition..."
    if ! echo isolated | $SUDO tee "$cg/cpuset.cpus.partition" > /dev/null; then
        explain_partition_failure "$name" "$cpus"
        die "Partition activation failed"
    fi

    local partition_state
    partition_state=$(cat "$cg/cpuset.cpus.partition")
    if [[ "$partition_state" != "isolated" ]]; then
        explain_partition_failure "$name" "$cpus"
        die "Partition state: $partition_state"
    fi

    state_add "$name" "$cpus"
    success "Partition '$name' active on CPUs $cpus (slice cgroup $cg)"
}

deactivate_partition() {
    local name="$1"
    local cg
    cg=$(partition_cgroup_path "$name")
    local unit
    unit=$(partition_slice_unit_path "$name")

    # Migration path: a legacy standalone cgroup also gets cleaned up.
    teardown_legacy_partition "$name"

    if [[ ! -d "$cg" ]] && [[ ! -f "$unit" ]]; then
        warn "slice ${name}.slice and cgroup $cg do not exist — removing from state only"
        state_remove "$name"
        return 0
    fi

    # Step down from isolated to member so systemctl stop doesn't fail
    # on a busy partition. Tasks are moved out by systemd as part of
    # the slice stop.
    if [[ -f "$cg/cpuset.cpus.partition" ]]; then
        echo member | $SUDO tee "$cg/cpuset.cpus.partition" > /dev/null 2>&1 || true
    fi
    if [[ -f "$cg/cpuset.cpus.exclusive" ]]; then
        : | $SUDO tee "$cg/cpuset.cpus.exclusive" > /dev/null 2>&1 || true
    fi

    if $SUDO systemctl is-active --quiet "${name}.slice"; then
        $SUDO systemctl stop "${name}.slice" || warn "systemctl stop ${name}.slice failed"
    fi

    if [[ -f "$unit" ]]; then
        $SUDO rm -f "$unit"
        $SUDO systemctl daemon-reload || true
        success "Removed $unit"
    fi

    state_remove "$name"
}

# ---------- Subcommand: create --------------------------------------------
cmd_create() {
    local name="" cpus="" with_ethercat=0 nic=""
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --with-ethercat-rt) with_ethercat=1; shift ;;
            --nic) nic="$2"; shift 2 ;;
            --yes) SKIP_PROMPTS=1; shift ;;
            -h|--help)
                echo "Usage: $0 create <name> <cpus> [--with-ethercat-rt] [--nic <iface>] [--yes]"
                return 0 ;;
            -*) die "Unknown flag: $1" ;;
            *)
                if [[ -z "$name" ]]; then name="$1"
                elif [[ -z "$cpus" ]]; then cpus="$1"
                else die "Unexpected positional argument: $1"
                fi
                shift ;;
        esac
    done

    [[ -z "$name" || -z "$cpus" ]] && die "Usage: create <name> <cpus> [options]"

    require_root
    preflight_cgroup_v2
    validate_partition_name "$name"
    validate_requested_cpus "$name" "$cpus"
    check_isolcpus_cmdline "$cpus"

    # If updating an existing partition, deactivate first.
    if state_get_cpus "$name" > /dev/null && [[ -n "$(state_get_cpus "$name")" ]]; then
        warn "Partition '$name' exists. Recreating."
        deactivate_partition "$name"
    fi

    activate_partition "$name" "$cpus"

    if [[ $with_ethercat -eq 1 ]]; then
        info ""
        info "Chaining into ethercat-rt setup..."
        cmd_ethercat_rt "$name" ${nic:+--nic "$nic"}
    fi
}

# ---------- Subcommand: remove --------------------------------------------
cmd_remove() {
    local name="${1:-}"
    [[ -z "$name" ]] && die "Usage: remove <name>"

    require_root
    preflight_cgroup_v2
    validate_partition_name "$name"

    local cpus
    cpus=$(state_get_cpus "$name")
    if [[ -z "$cpus" ]]; then
        warn "Partition '$name' not found in state. Attempting cgroup cleanup anyway."
    else
        info "Removing partition '$name' (cpus $cpus)..."
    fi

    deactivate_partition "$name"
}

# ---------- Subcommand: list -----------------------------------------------
cmd_list() {
    local names
    names=$(state_list_names)
    if [[ -z "$names" ]]; then
        echo "No managed partitions."
        return 0
    fi

    printf "%-20s %-15s %-15s %s\n" NAME CPUS STATE CGROUP_PATH
    printf "%-20s %-15s %-15s %s\n" ---- ---- ----- -----------
    local name cpus state cg
    while read -r name; do
        [[ -z "$name" ]] && continue
        cpus=$(state_get_cpus "$name")
        cg=$(partition_cgroup_path "$name")
        if [[ -f "$cg/cpuset.cpus.partition" ]]; then
            state=$(cat "$cg/cpuset.cpus.partition" 2>/dev/null || echo ?)
        elif [[ -d "$CGROUP_ROOT/$name" ]]; then
            # Pre-Option-C standalone cgroup still around — report it so the
            # operator sees the migration hasn't completed.
            state="legacy (pre-slice)"
            cg="$CGROUP_ROOT/$name"
        else
            state="MISSING"
        fi
        printf "%-20s %-15s %-20s %s\n" "$name" "$cpus" "$state" "$cg"
    done <<< "$names"
}

