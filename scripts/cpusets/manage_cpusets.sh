      activation (and --nic picks the iface, defaults to ecat0).
      --yes skips the isolcpus overlap prompt.
      Example:  sudo $prog create rt 13

  remove <name>
      Deactivate the partition, move any tasks still inside it back to
      the root cgroup, and delete the cgroup directory. Sibling slices
      are re-expanded to cover the newly freed cores.
      Example:  sudo $prog remove rt

Query:
  list
      Table of managed partitions: name, CPUs, current partition state
      (isolated / member / MISSING), and cgroup path.

  status
      Global isolation snapshot: present and online CPUs, isolcpus= /
      nohz_full= / rcu_nocbs= / irqaffinity= from /proc/cmdline, sysfs
      isolated set, computed housekeeping cores, managed partitions,
      systemd affinity drop-in, cpusets.service state.

Execution:
  run <name> -- <cmd...>
      Place the invoking shell into the named partition's cgroup, then
      exec <cmd>. The command inherits the partition's CPU set. Useful
      for ad-hoc testing and one-off pinned runs; for permanent
      services use a systemd unit with AllowedCPUs= instead.
      Example:  sudo $prog run rt -- cyclictest -p 95 -t 1 -a 13

Batch config:
  apply <config-file> [--yes]
      Read an INI config file and reconcile partitions to match it.
      Two-pass: validates every section (names, CPU ranges, overlap,
      housekeeping floor) before mutating anything. Already-correct
      partitions are skipped. On mid-run failure, partitions created
      during this invocation are rolled back. --yes skips interactive
      prompts.
      INI format:
        [ecat]
        cpus = 10-11
        description = EtherCAT master

Verification:
  verify [<name>]
      Run the verification suite. Exits nonzero on any failure.
        Phase 0:   cgroup v2 mounted, cpuset controller present.
        Phase 1/2: partition state = 'isolated', effective CPUs
                   match requested, sibling slices don't claim the
                   partition's CPUs.
        Phase 3:   no unexpected userspace tasks on isolated cores.
        Phase 3.5: systemd affinity drop-in present and matches the
                   current housekeeping set.
      If <name> is omitted, verifies all managed partitions.

EtherCAT integration:
  ethercat-rt <name> [--nic <iface>]
      On an existing partition: pin the NIC's IRQs to one of its
      cores, apply ethtool low-latency tuning (100M/full-duplex, no
      autoneg, coalescing off, offloads off, small rings, napi defer
      on), install the boot-persistence service, lock the performance
      governor on every isolated core, and restrict unbound
      workqueues to housekeeping cores. --nic defaults to ecat0. The
      partition must already exist (use 'create' first, or use
      'create --with-ethercat-rt' to do both in one step).

Boot persistence:
  install-service [<config-file>]
      Install /etc/systemd/system/cpusets.service so partitions
      activate at boot. Copies this script and lib/*.sh to
      /usr/local/lib/manage_cpusets/ so the service is self-contained
      and survives the source tree moving. Service ordering:
      Before=docker.service user@.service systemd-logind.service
      so their cgroups cannot claim the isolated CPUs before the
      partition is active. If <config-file> is omitted, the built-in
      default ([${DEFAULT_PARTITION_NAME}] cpus=${DEFAULT_PARTITION_CPUS})
      is materialized to /etc/cpusets.conf.
      Example:  sudo $prog install-service /etc/cpusets.conf

  uninstall-service
      Disable the service and remove the installed scripts, config
      copy (/etc/cpusets.conf), wrapper
      (/usr/local/sbin/apply-cpusets), and unit file.

systemd default affinity:
  install-affinity-defaults
      Write /etc/systemd/system.conf.d/cpuaffinity.conf with
      [Manager] CPUAffinity=<housekeeping>. This restricts the
      default CPU affinity of every systemd-spawned service to
      non-isolated cores, keeping stray processes off your RT cores.
      Needs 'systemctl daemon-reexec' or a reboot to take effect for
      services started before this edit.

  uninstall-affinity-defaults
      Remove the drop-in. daemon-reexec / reboot to revert.

Kernel cmdline editing:
  migrate-cmdline [--add-rt-flags] [--yes]
      Edit the kernel cmdline to remove isolcpus=*. Detects bootloader
      (Jetson /boot/extlinux/extlinux.conf or GRUB /etc/default/grub),
      makes a timestamped backup, shows the proposed diff, and applies
      atomically after confirmation. --add-rt-flags also injects
      rcu_nocb_poll, skew_tick=1, and irqaffinity=<housekeeping>.
      For GRUB, runs update-grub after. Reboot required for the new
      cmdline to take effect.

Help:
  help, -h, --help
      Show this message.

Examples:
  # Single partition + pinned command:
  sudo $prog create rt 13
  sudo $prog run rt -- ./my_program

  # Multi-partition from config:
  sudo $prog apply /etc/cpusets.conf

  # Full migration from isolcpus= to cpuset partitions:
  sudo $prog install-service /etc/cpusets.conf
  sudo $prog install-affinity-defaults
  sudo $prog migrate-cmdline --add-rt-flags
  sudo reboot
EOF
}

# ---------- Dispatch -------------------------------------------------------
main() {
    local cmd="${1:-help}"
    shift || true

    case "$cmd" in
        create)                        cmd_create "$@" ;;
        remove)                        cmd_remove "$@" ;;
        list)                          cmd_list "$@" ;;
        status)                        cmd_status "$@" ;;
        run)                           cmd_run "$@" ;;
        apply)                         cmd_apply "$@" ;;
        verify)                        cmd_verify "$@" ;;
        ethercat-rt)                   cmd_ethercat_rt "$@" ;;
        install-service)               cmd_install_service "$@" ;;
        uninstall-service)             cmd_uninstall_service "$@" ;;
        uninstall-runtime)             cmd_uninstall_runtime "$@" ;;
        install-affinity-defaults)     cmd_install_affinity_defaults "$@" ;;
        uninstall-affinity-defaults)   cmd_uninstall_affinity_defaults "$@" ;;
        migrate-cmdline)               cmd_migrate_cmdline "$@" ;;
        help|-h|--help)                cmd_help ;;
        *)
            error "Unknown subcommand: $cmd"
            cmd_help
            exit 1
            ;;
    esac
}

main "$@"
